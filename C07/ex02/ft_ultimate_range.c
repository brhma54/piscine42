#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>
#include <string.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	*tab;
	int	i;

	i = 0;
	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	tab = (int *)malloc(sizeof(int) * ((max - min) + 1));
	if (tab == NULL)
		return (-1);
	else
	{
		i = -1;
		while (i++ < (max - min) - 1)
			tab[i] = min + i;
		*range = tab;
		return (i);
	}
}

